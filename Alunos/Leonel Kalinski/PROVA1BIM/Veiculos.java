package objetos;

public class Veiculos {
	private int placa;
	private int valdia;
	
	public Veiculos() {}

	public int getPlaca() {
		return placa;
	}

	public void setPlaca(int placa) {
		this.placa = placa;
	}

	public int getValdia() {
		return valdia;
	}

	public void setValdia(int valdia) {
		this.valdia = valdia;
	}
}
